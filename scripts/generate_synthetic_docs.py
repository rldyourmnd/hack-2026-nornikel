def write_pdf_reports(manifest: dict[str, Any]) -> None:
    """EN-language text-layer PDFs via reportlab (built-in Helvetica, no font vendoring).

    Russian narrative lives in DOCX/MD fixtures; the PDF path is proven with EN
    text plus one image-only page that MUST quarantine.
    """
