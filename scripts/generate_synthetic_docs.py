    from reportlab.pdfgen.canvas import Canvas

    reports = {
        "v2_report_nicu_aging_en.pdf": [
            "Synthetic aging study of Ni-30Cu (a.k.a. Ni30Cu).",
            "Aging at 700 C for 8 h in air raised Vickers hardness",
            "from 210 HV to 246 HV (method HV10).",
            "Performed by the Synthetic metallurgy team, resistance furnace.",
            "Conclusion dated 2024-09-12.",
        ],
        "v2_report_nicrmo_rolling_en.pdf": [
            "Cold rolling of the Ni-Cr-Mo demo alloy (NiCrMo-D).",
            "Tensile strength grew from 760 MPa to 905 MPa (ISO 6892).",
            "Corrosion rate was NOT measured for this alloy.",
            "Conclusion dated 2025-02-14.",
        ],
    }
    for filename, lines in reports.items():
        canvas = Canvas(str(OUT / filename), pagesize=A4)
        canvas.setFont("Helvetica", 12)
        y = 800
        for line in lines:
            canvas.drawString(60, y, line)
            y -= 22
        canvas.showPage()
        canvas.save()
        manifest["sources"][filename] = {"kind": "pdf", "language": "en"}

    # Image-only page: no text operators at all -> honest quarantine.
    scan = Canvas(str(OUT / "v2_scan_no_text_layer.pdf"), pagesize=A4)
    scan.setFillGray(0.85)
    scan.rect(40, 400, 500, 380, fill=1, stroke=0)
    scan.showPage()
    scan.save()
    manifest["sources"]["v2_scan_no_text_layer.pdf"] = {
        "kind": "pdf",
        "expected_status": "quarantined",
    }


